class Calculations:
    def __init__(self, a, b):
        self.a = a
        self.b = b
    
    def getSum(self):
        return self.a + self.b
    
    def getDifference(self):
        return self.a -self.b
    
    def getProduct(self):
        return self.a * self.b
    
    def getQuotient(self):
        return self.a / self.b